    private void btnCadastrarOrdemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarOrdemActionPerformed
        String descricao = this.txtOrdem.getText();
        
        JSONObject json = new JSONObject();
        
        json.put("descricao", descricao);
        json.put("operacao", "cadastrar_ordem");
        json.put("token", token);
        
        enviarJson(json);
        JSONObject respostaJson = receberJson();
        
        if (respostaJson.getString("status").equals("sucesso")) {
            JOptionPane.showMessageDialog(this, "Ordem cadastrada com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            MenuCliente menuCliente = new MenuCliente(socket, output, input, token);
            menuCliente.setVisible(true);
            this.setVisible(false);
        } else if (respostaJson.getString("status").equals("erro")) {
            JOptionPane.showMessageDialog(this, respostaJson.getString("mensagem"), "erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnCadastrarOrdemActionPerformed

    /**
     * @param args the command line arguments
     */
    
    private void enviarJson(JSONObject json) {
        output.println(json.toString());
        System.out.println("JSON enviado: " + json.toString());
    }

    private JSONObject receberJson() {
        String resposta;
        try {
            resposta = input.readLine();
            JSONObject respostaJson = new JSONObject(resposta);
            System.out.println("JSON recebido: " + respostaJson.toString());
            return respostaJson;
        } catch (IOException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        return null;
    }
    
